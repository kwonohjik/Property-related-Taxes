/**
 * 토지등급 번호로 등급가액 조회.
 * 1~365 범위 밖이면 `TaxCalculationError`.
 */
export function getGradeValue(grade: number): number {
  if (!Number.isInteger(grade)) {
    throw new TaxCalculationError(
      TaxErrorCode.INVALID_INPUT,
      `토지등급은 정수여야 합니다 (입력: ${grade})`,
    );
  }
  if (grade < LAND_GRADE_MIN || grade > LAND_GRADE_MAX) {
    throw new TaxCalculationError(
      TaxErrorCode.INVALID_INPUT,
      `토지등급은 ${LAND_GRADE_MIN}~${LAND_GRADE_MAX} 범위여야 합니다 (입력: ${grade})`,
    );
  }
  const v = LAND_GRADE_VALUES[grade];
  if (v === undefined) {
    throw new TaxCalculationError(
      TaxErrorCode.INVALID_INPUT,
      `등급가액 테이블에 ${grade}등급이 없습니다 (테이블 누락)`,
    );
  }
  return v;
}
