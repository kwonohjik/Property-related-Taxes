/** 최저·최고 등급 */
export const LAND_GRADE_MIN = 1;
export const LAND_GRADE_MAX = 365;

/** 365등급 초과 외삽 규칙 (비고): 200,000,000원 이상은 6,000,000원 증가마다 1등급 상승 */
