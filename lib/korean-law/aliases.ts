/**
 * 법령명 해석. 매핑에 없으면 원문을 그대로 반환 (이미 정식명이거나 미지 법령).
 */
export function resolveLawAlias(input: string): string {
  const trimmed = input.trim();
  return LAW_ALIASES[trimmed] ?? trimmed;
}

/**
 * 약칭 여부 판별 (정식명과 다른 경우 true).
 */
export function isAlias(input: string): boolean {
  const resolved = resolveLawAlias(input);
  return resolved !== input.trim();
}
